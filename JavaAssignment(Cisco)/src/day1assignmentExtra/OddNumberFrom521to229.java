package day1assignmentExtra;

public class OddNumberFrom521to229 {

	public static void main(String[] args) {
		// wap to print odd number from 521 to 229 using while loop.
		
		int n=521;
		while(n>=229)
		{
			if(n%2!=0)
			{
				System.out.println("Odd number from 521 to 229 "+n);
				
			}
			n--;
		}
		
		
		

	}

}
