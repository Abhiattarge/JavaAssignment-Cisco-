package day4practiceinclass;

public class OddBetween25To75WhileLoop {

	public static void main(String[] args) {
		//1. Print odd number between 25 to 75 using while loop
		 
		 int i=25;
		 
		 System.out.println("Odd Number from 25 to 75 ");
		 while(i<=75)
		 {
				 System.out.println(i+ " ");
				 i=i+2;
		 }
	}

}
