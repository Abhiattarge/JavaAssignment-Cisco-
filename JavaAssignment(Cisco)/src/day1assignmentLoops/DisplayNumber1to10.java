package day1assignmentLoops;

public class DisplayNumber1to10 {

	public static void main(String[] args) {
		// write a java program to display number from 1 to 10 using for loop.
		
		for(int i = 1; i <= 10; i++)
		{
			
			System.out.println(i);
		}
		System.out.println("Displayed Number is 1 to 10: ");

	}

}
