package day4assignment;

public class RelationalOperator {

	public static void main(String[] args) {
		// 3. Relational Operator
		
		int a = 7, b = 11;
		
		System.out.println("a is "+ a +"and b is "+ b);
		
		System.out.println(a == b);
		
		System.out.println(a != b);
		
		System.out.println( a > b);
		
		System.out.println(a < b);
		
		System.out.println(a >= b);
		
		System.out.println(a <= b);

	}

}
