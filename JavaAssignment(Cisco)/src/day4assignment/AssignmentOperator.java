package day4assignment;

public class AssignmentOperator {

	public static void main(String[] args) {
		//2. Assignment Operators
		
				int c = 4;
				int var;
				
				var = c;
				System.out.println("var using =:"+ var);
				
				var += c;
				System.out.println("var using =:"+ var);
				
				var *= c;
				System.out.println("var using =:"+ var);
	}

}
