package day4assignment;

public class TernaryOperator {

	public static void main(String[] args) {
		//conditional AND
		//conditional OR
		// Conditional Operator Ternary Operator
		int x,y;
		
		x=20;
		
		y=(x==1)?61:90;
		System.out.println("value of y is: "+ y);
		
		y=(x==20)?61:90;
		System.out.println("value of y is: "+ y);

	}

}
